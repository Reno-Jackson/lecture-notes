// For Loop Practice 1

// Question 1.) 0 to 500 incrementing by the hundreds

for( var i = 0; i <=500; i+=100){
    console.log(i);
}

// Question 2.) 1 to 64 doubling

for (var i=1; i <=64; i=i*2){
    console.log(i);
}

// Question 3.) 1 to 3 each number loops 3 times

for (var i=1; i <=3; i++){
    for(var b=1; b<4; b++){
        console.log(i);
    }
    console.log(b);
}

